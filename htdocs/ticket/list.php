							$error++;
							break;
						} else {
							$nbok++;
						}
					} else {
						$langs->load("errors");
						setEventMessages($langs->trans("ErrorObjectMustHaveStatusClosedToBeReOpened", $objecttmp->ref), null, 'errors');
